using Microsoft.AspNetCore.Http;
using Microsoft.AspNetCore.Mvc;
using TeddyBear.Repository;

namespace TeddyBear.Controllers
{
    public class CategoriesController : Controller
    {
        // GET: CategoriesController
        public ActionResult Index(int id, int idCate)
        {
            if (idCate != null)
            {
                ViewBag.Prod = ProductRepo.Instance().GetProductByIdCate(idCate);
            }
            else
            {
                ViewBag.Prod = ProductRepo.Instance().GetProductByIdSubCate(id);
            }
            return View();
        }

        // GET: CategoriesController/Details/5
        public ActionResult Details(int id)
        {
            return View();
        }

        // GET: CategoriesController/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: CategoriesController/Create
